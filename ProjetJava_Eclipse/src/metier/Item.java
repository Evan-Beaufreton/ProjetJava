package metier;

public class Item {
	int idItem ;
	String nom;
	String efficacite;
	String description;
	public Item(int idItem,String nom,String efficacite,String description ) {
		
		this.idItem=idItem;
		this.nom=nom;
		this.efficacite=efficacite;
		this.description=description;
		
		
		
		
		
	}
	@Override
	public String toString() {
		return "Item [idItem=" + idItem + ", nom=" + nom + ", efficacite=" + efficacite + ", description=" + description
				+ "]";
	}
	public int getIdItem() {
		return idItem;
	}
	public void setIdItem(int idItem) {
		this.idItem = idItem;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getEfficacite() {
		return efficacite;
	}
	public void setEfficacite(String efficacite) {
		this.efficacite = efficacite;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

}
